    public void atualizarTabela() {
        listaPacientes = dao.getPacientes();
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"ID", "Nome", "CPF", "Telefone", "Data Nasc."}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Impede a edição direta na tabela
            }
        };

        if (listaPacientes != null) {
            for (Paciente p : listaPacientes) {
                modelo.addRow(new Object[]{p.getId(), p.getNome(), p.getCpf(), p.getTelefone(), p.getDataNascimento(), p.getEmail()});
            }
        }
        tblPacientes.setModel(modelo);
    }



